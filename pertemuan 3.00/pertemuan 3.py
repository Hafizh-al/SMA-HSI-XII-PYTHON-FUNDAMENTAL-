# = (Operator Assignment Tunggal):
# Fungsi: Untuk memberi nilai atau mengisi variabel.
# Cara Baca: "Masukkan nilai yang ada di kanan ke dalam variabel di kiri."
# Sifat: Sebuah aksi atau perintah.
# Contoh: umur = 17 (Perintah: "Jadikan umur bernilai 17!")

# == (Operator Perbandingan Ganda):
# Fungsi: Untuk membandingkan kesamaan dua nilai.
# Cara Baca: "Apakah nilai di kiri sama dengan nilai di kanan?"
# Sifat: Sebuah pertanyaan yang menghasilkan True atau False .
# Contoh: umur == 17 (Pertanyaan: "Apakah umur sama dengan 17?")

